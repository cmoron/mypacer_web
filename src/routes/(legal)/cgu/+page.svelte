<h1>Conditions Générales d'Utilisation</h1>

<p>
  Les présentes Conditions Générales d'Utilisation (ci-après « CGU ») définissent les modalités et conditions dans
  lesquelles toute personne (ci-après « l'Utilisateur ») peut accéder au site
  <a href="https://mypacer.fr">mypacer.fr</a> et au service <strong>MyPacer Club</strong> édités par DecaSaaS. L'accès au
  site et l'utilisation du service valent acceptation pleine et entière des présentes CGU.
</p>

<h2>Article 1 — Objet</h2>

<p>
  Les CGU régissent l'utilisation du service MyPacer Club, qui propose la génération automatisée et l'envoi par email de
  rapports hebdomadaires des résultats sportifs des clubs affiliés à la Fédération Française d'Athlétisme (ci-après «
  FFA »), à partir des données publiques publiées sur
  <a href="https://www.athle.fr" rel="noopener" target="_blank">www.athle.fr</a>.
</p>

<p>
  Les conditions financières de souscription à ce service font l'objet d'un document distinct, les
  <a href="/cgv">Conditions Générales de Vente</a>.
</p>

<h2>Article 2 — Description du service</h2>

<p>Le service MyPacer Club fournit automatiquement à ses abonnés, sur une base hebdomadaire :</p>

<ul>
  <li>un rapport email compilant les résultats de compétition du club pour la semaine écoulée ;</li>
  <li>une mise en avant des podiums, qualifications et performances de niveau national ou interrégional ;</li>
  <li>un mécanisme de rattrapage (« catchup ») pour les résultats publiés avec retard par la FFA.</li>
</ul>

<p>
  Le site <a href="https://mypacer.fr">mypacer.fr</a> met également à disposition, en libre accès, des outils complémentaires
  à l'attention de la communauté des coureurs (calculateur d'allures, recherche de licenciés, etc.), dont l'utilisation est
  gratuite et soumise aux présentes CGU.
</p>

