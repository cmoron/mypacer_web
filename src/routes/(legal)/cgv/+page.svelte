<h2>Article 3 — Description du Service</h2>

<p>Le service MyPacer Club comprend, pour chaque semaine du cycle d'abonnement :</p>

<ul>
  <li>
    la <strong>collecte automatisée</strong> des résultats de compétition du club du Client depuis les données publiques de
    la FFA ;
  </li>
  <li>
    l'<strong>analyse et la mise en forme</strong> de ces résultats dans un rapport HTML comprenant notamment podiums, qualifications
    et performances remarquables ;
  </li>
  <li>
    l'<strong>envoi par courrier électronique</strong> de ce rapport aux adresses déclarées par le Client, dans la limite
    définie par la formule souscrite ;
  </li>
  <li>
    un <strong>mécanisme de rattrapage</strong> (« catchup ») couvrant les résultats publiés postérieurement à l'envoi hebdomadaire
    initial.
  </li>
</ul>

<p>
