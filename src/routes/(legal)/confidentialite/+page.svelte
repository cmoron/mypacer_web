<h2>Mesures de sécurité</h2>

<p>
  Conformément à l'article 32 du RGPD, DecaSaaS met en œuvre les mesures techniques et organisationnelles appropriées
  pour garantir un niveau de sécurité adapté au risque présenté par les traitements. Ces mesures incluent notamment :
</p>

<ul>
  <li>
    <strong>chiffrement des communications en transit</strong> via HTTPS/TLS pour l'ensemble des échanges avec le site, l'infrastructure
    applicative et les services de sous-traitants ;
  </li>
  <li>
    <strong>hébergement de l'infrastructure principale en Union européenne</strong> (OVH, France), dans des centres de données
    certifiés ISO/IEC 27001 ;
  </li>
  <li>
    <strong>contrôle d'accès strict</strong> aux données personnelles, limité aux seules personnes dûment habilitées au sein
    de DecaSaaS, sur la base du principe du moindre privilège ;
  </li>
  <li>
    <strong>journalisation des accès administratifs</strong> et des opérations sensibles, avec rotation automatique des journaux
    techniques ;
  </li>
  <li>
    <strong>séparation des environnements</strong> (développement, pré-production, production) et gestion versionnée du code
    source.
  </li>
</ul>

<p>
  Ces mesures font l'objet d'un réexamen périodique et sont adaptées à l'évolution des risques et des bonnes pratiques
  de l'état de l'art.
</p>

