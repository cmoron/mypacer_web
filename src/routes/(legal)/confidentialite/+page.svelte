<h3>3. Données d'athlètes : position de principe</h3>

<div class="callout">
  <p>
    <strong
      >MyPacer Club ne collecte, ne stocke et ne traite aucune donnée personnelle d'athlète au sens de l'article 4 du
      RGPD.</strong
    >
  </p>
</div>

<p>Concrètement :</p>

<ul>
  <li>
    Les résultats sportifs (nom, performance, classement) sont <strong>extraits à la volée</strong>
    depuis la source publique <a href="https://www.athle.fr" rel="noopener" target="_blank">www.athle.fr</a>
    à chaque génération de rapport, mis en forme dans un courrier électronique, puis transmis aux abonnés du club.
    <strong>Aucune copie n'est conservée</strong> par MyPacer Club après envoi.
  </li>
  <li>
    Seules sont conservées, pour une durée maximale de <strong>cinq (5) semaines</strong> et à des fins strictement
    techniques de déduplication entre l'envoi hebdomadaire et le mécanisme de rattrapage, des
    <strong>empreintes cryptographiques SHA-256 tronquées à 48 bits</strong>. Ces empreintes, par construction, ne
    permettent pas de remonter à l'identité d'un athlète ni à son résultat.
  </li>
  <li>
    Les journaux techniques (logs) de l'application ne conservent, pour chaque envoi, que l'identifiant du club et
    l'adresse email du destinataire — à l'exclusion de toute donnée relative à un athlète.
  </li>
</ul>

<p>
  Dans ces conditions, MyPacer Club <strong>n'est pas responsable de traitement au sens du RGPD</strong>
  à l'égard des athlètes dont les résultats sont relayés dans les rapports. Les données sont publiées par la Fédération Française
  d'Athlétisme, seule responsable de traitement, sur la base du consentement recueilli lors de la prise de licence fédérale.
</p>

<p>
  Toute demande relative à la publication, la modification ou la suppression d'un résultat sur
  <a href="https://www.athle.fr" rel="noopener" target="_blank">www.athle.fr</a> doit être adressée
  <strong>directement à la Fédération Française d'Athlétisme</strong>.
</p>

