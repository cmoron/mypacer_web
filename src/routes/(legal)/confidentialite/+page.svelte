<h2>Notification en cas de violation de données</h2>

<p>
  En cas de <strong>violation de données à caractère personnel</strong> au sens de l'article 4.12 du RGPD (perte, destruction,
  altération, divulgation non autorisée ou accès non autorisé à des données), susceptible d'engendrer un risque pour les droits
  et libertés des personnes concernées, DecaSaaS s'engage à :
</p>

<ul>
  <li>
    <strong>notifier la violation à la CNIL</strong> dans un délai maximal de <strong>72 heures</strong> à compter de sa prise
    de connaissance, conformément à l'article 33 du RGPD ;
  </li>
  <li>
    <strong>informer sans délai indu les personnes concernées</strong> lorsque la violation est susceptible d'engendrer un
    risque élevé pour leurs droits et libertés, conformément à l'article 34 du RGPD, en décrivant en des termes clairs et
    simples la nature de la violation, ses conséquences probables et les mesures prises ou envisagées pour y remédier ;
  </li>
  <li>
    <strong>documenter chaque violation</strong> (nature, conséquences, mesures prises) dans un registre interne, afin de
    permettre à la CNIL d'en vérifier la conformité.
  </li>
</ul>

